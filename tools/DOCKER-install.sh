# Docker installation script
# Use this script to install Docker for building on a local agent.
#
/usr/bin/apt-get remove docker docker-engine docker.io containerd runc

/usr/bin/apt-get update

/usr/bin/apt-get install -yq \
    ca-certificates \
    curl \
    gnupg
    
/usr/bin/install -m 0755 -d /etc/apt/keyrings
/usr/bin/curl -fsSL https://download.docker.com/linux/ubuntu/gpg | /usr/bin/gpg --dearmor -o /etc/apt/keyrings/docker.gpg
/usr/bin/chmod a+r /etc/apt/keyrings/docker.gpg

/usr/bin/echo \
  "deb [arch="$(dpkg --print-architecture)" signed-by=/etc/apt/keyrings/docker.gpg] https://download.docker.com/linux/ubuntu \
  "$(. /etc/os-release && echo "$VERSION_CODENAME")" stable" | \
  tee /etc/apt/sources.list.d/docker.list > /dev/null
  
/usr/bin/apt-get update

/usr/bin/apt-get install -yq docker-ce docker-ce-cli containerd.io docker-buildx-plugin docker-compose-plugin

/usr/bin/docker run hello-world
